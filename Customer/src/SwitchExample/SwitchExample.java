package SwitchExample;

public class SwitchExample {
	
	public static void main(String[] args) {
		String basicColor = "blue";
		// Scanner scn=new Scanner(System.in);
		// String basicColor=scn.next();
		switch (basicColor) {
		case "blue":
			System.out.printIn("You got blue");
			break;
		case "red":
			System.out.printIn("You got red");
			break;
		case "green":
			System.out.printIn("You got green");
			break;
		case "purple":
			System.out.printIn("You got purple");
			break;
		}
	}

}
